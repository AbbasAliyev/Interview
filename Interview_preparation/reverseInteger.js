let str = 'Hello';
let reverseStr = str.split(""); //The split() method splits a String object into an array of string by separating the string into sub strings.
let reverseArr = reverseStr.reverse(); //The reverse() method reverses an array in place. The first array element becomes the last and the last becomes the first.
let joinArr = reverseArr.join(""); //The join() method joins all elements of an array into a string.


console.log(joinArr)

